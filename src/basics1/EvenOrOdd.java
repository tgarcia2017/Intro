package basics1;

public class EvenOrOdd {

	public static void main(String[] args) {
		evenOrOdd(7);
	}
		static void evenOrOdd(int i) {
			
			if (i%2 == 0) {
				System.out.println("Number is even");
			}
			
			else {
				System.out.println("Number is odd");
			}
		}
		
		
	}
